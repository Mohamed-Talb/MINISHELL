#include "../libft.h"

void	ft_freedouble(char **ptr)
{
	int	i;

	i = 0;
	if (!ptr || !*ptr)
		return ;
	while (ptr[i])
	{
		ft_free(ptr[i]);
		i++;
	}
	ft_free(ptr);
}
